const month = 6;
switch (month) {
    case 1:
        console.log("January");        
        break;
    case 2:
        console.log("February");        
        break;
    case 3:
        console.log("March");        
        break;
    case 6:
        console.log("June");        
        break;
    case 7:
        console.log("July");        
        break;
    case 8:
        console.log("August");        
        break;

    default:
        console.log("default month match...");
        break;
}